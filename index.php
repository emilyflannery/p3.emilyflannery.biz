	<h4>Ingredients</h4>
	<ul>
		<li><span class="ingredient">1</span> pounds Green Beans, Trimmed And Cut Into Bite Sized Pieces</li>
		<li><span class="ingredient">8</span> ounces White Mushrooms, Sliced</li>
		<li><span class="ingredient">2</span> Tablespoons Butter</li>
		<li><span class="ingredient">2</span> clove Garlic, Minced</li>
		<li><span class="ingredient">1.3</span> cups Heavy Cream</li>
		<li><span class="ingredient">.5</span> cups Parmesan Cheese, Shredded</li>
		<li><span class="ingredient">.25</span> teaspoons Black Pepper</li>
		<li><span class="ingredient">1</span> pinch Ground Nutmeg</li>
		<li><span class="ingredient">1</span> cup French Fried Onions</li>
	</ul>
	<h4>Directions</h4>
	<p>Heat a pot of water to boiling and place beans in the water to blanch for about 5 minutes. Beans should be bright green and just barely tender. Immediately remove beans from water and plunge into a a bowl of cold water or run under the faucet until cool. This stops the cooking process.
	In a medium pan, saute mushrooms in butter and salt until soft and starting to caramelize. Stir garlic and cream into the mushrooms and bring to a simmer. Stir in Parmesan, pepper, nutmeg, and green beans and continue to simmer for about 5 minutes or until the the sauce is thickened and the beans are tender.
	Remove from heat, season with more salt to taste and top with french-fried onions.</p>
